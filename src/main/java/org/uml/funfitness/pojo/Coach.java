package org.uml.funfitness.pojo;

public class Coach {
    private Integer coachAccount;
    private String coachPassword;
    private String coachName;
    private String coachGender;
    private Integer coachAge;
    private Integer coachHeight;
    private Integer coachWeight;

    public Integer getCoachAccount() {
        return coachAccount;
    }

    public void setCoachAccount(Integer coachAccount) {
        this.coachAccount = coachAccount;
    }

    public String getCoachPassword() {
        return coachPassword;
    }

    public void setCoachPassword(String coachPassword) {
        this.coachPassword = coachPassword;
    }

    public String getCoachName() {
        return coachName;
    }

    public void setCoachName(String coachName) {
        this.coachName = coachName;
    }

    public String getCoachGender() {
        return coachGender;
    }

    public void setCoachGender(String coachGender) {
        this.coachGender = coachGender;
    }

    public Integer getCoachAge() {
        return coachAge;
    }

    public void setCoachAge(Integer coachAge) {
        this.coachAge = coachAge;
    }

    public Integer getCoachHeight() {
        return coachHeight;
    }

    public void setCoachHeight(Integer coachHeight) {
        this.coachHeight = coachHeight;
    }

    public Integer getCoachWeight() {
        return coachWeight;
    }

    public void setCoachWeight(Integer coachWeight) {
        this.coachWeight = coachWeight;
    }

    public Long getCoachPhone() {
        return coachPhone;
    }

    public void setCoachPhone(Long coachPhone) {
        this.coachPhone = coachPhone;
    }

    private Long coachPhone;

}
